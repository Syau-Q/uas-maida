				<img src="images/i.jpg" class="img-thumbnail img-responsive"><p>

						<p>Bahasa Inggris adalah bahasa internasional yang sudah banyak digunakan untuk berkomunikasi di sebagian besar negara, bahkan banyak negara yang sudah menjadikan bahasa Inggris sebagai bahasa yang harus ada dalam mata pelajaran di sekolah, termasuk Indonesia. Tetapi sayangnya, banyak yang beranggapan bahwa bahasa Inggris hanya identik dengan budaya Inggris saja, sehingga kita seolah-olah dituntut untuk mengikuti budaya sang penutur asli. Padahal, kita sebagai seorang guru bisa mengajarkan tokoh-tokoh serta nilai-nilai keislaman menggunakan bahasa Inggris.<p>

						Bagaimana caranya?<p>

						Yuk simak 4 cara sederhana yang bisa dilakukan seorang guru untuk mengajarkan nilai-nilai Islam pada pembelajaran bahasa Inggris.<p>

						1. Ketika memberikan contoh kalimat, gunakan nama-nama tokoh Islam<p>
						Contoh untuk materi Present Tense:<p>

						- <i>Hamzah and Umar are strong</i><p>
						- <i>Abu Bakar goes to Ka’bah to do Hajj</i><p>
						- <i>Muslim living all over the world love Palestine</i><p>
						<br>

						Contoh untuk materi Past Tense:<p>

						- <i>Hamzah was syahid in the battle of Uhud.</i><p>
						- <i>Bilal Bin Rabbah killed Umayyah bin Khalaf in the battle of Badr.</i><p>
						- <i>Rasulullah led Salah for all the prophets in Masjidil Aqsha in the night of Mi’raj.</i><p>
						<br>

						2. Ketika memberikan contoh berupa dialog, contohkan dialog yang bernuansa Islami.<p>
						Berikut contoh untuk materi “expressing offers”:<p>

						Khalid	:	<i> Bilal, you look so tired, what are you doing?</i><p>
						Bilal	:	<i> I am looking for my Qur’an, but I couldn’t find it.</i><p>
						Khalid	:	<i> May I help you to find it?</i><p>
						Bilal	:	<i> Sure, it will make me happy.</i><p>
						Khalid	:	<i> Ok Bilal. Because you are here, I will check it over there.</i><p>
						Bilal	:	<i> Thank you so much Khalid.</i><p>
						Khalid	:	<i> You’re welcome.</i><p>
						<br>

						Ketika memberikan contoh teks, buatlah teks Islami yang ringan untuk dibaca.<p>
						Berikut contoh untuk materi “Descriptive Text: Describing Place”.<p>

						<b>Istiqlal Mosque<p>

						Istiqlal mosque is a pride for Indonesia, because this mosque is the largest mosque in Southeast Asia. This mosque was built decades ago, but still has that charm to welcome every Muslim to come and pray.  This mosque is located at Jalan Taman Wijaya Kusuma, Central Jakarta. It stands almost right across the old Catholic Church. These landmarks represent the nation’s acceptance of religious diversity. When you enter the mosque, you will find seven entrances that you can go through to choose. Each of the entrance symbolizes one of the seven heavens of Islam.<p>

						Untuk aktivitas listening, carilah recording yang memiliki nilai Islami juga. Untuk membuatnya lebih bervariatif, biasanya sang guru akan menemukan sebuah lagu yang cocok dengan materi yang diajarkan. Nah, untuk pemilihan lagu tidak harus lagu bernuansa cinta atau patah hati pada umumnya. Ada banyak rekomendasi lagu-lagu Islami berbahasa Inggris yang bisa menjadi pilihan, salah satunya lagu “Omar Esa- Allah Allah” untuk mempelajari present tense. Untuk aktifitas yang akan dilakukan yaitu dengan merumpangkan kata yang menggunakan present tense agar siswa bisa melihat perubahan kalimat pada subject yang berbeda.<p>

						I don’t _______ it so goodbye (want)<p>

						I ______ want to live no lie (don’t)<p>

						Turning back to Him<p>

						He _______ everything, Oh Allah (gives)<p></b>

